package com.yunat.ccms.metadata.face;
/**
 * 接口对象：显示布尔结果
 * @author kevin.jiang
 * 2013-3-16
 */
public class FaceBoolean {

	private boolean val;

	public FaceBoolean(boolean value){
		this.val = value;
	}

	public boolean isVal() {
		return val;
	}

	public void setVal(boolean val) {
		this.val = val;
	}

}
