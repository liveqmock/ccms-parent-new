package com.yunat.ccms.metadata.face;

/**
 * 接口对象：界面显示的字段
 * 
 * @author kevin.jiang 2013-3-15
 */
public class FaceAttribute {

	private String key;
	private String name;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
