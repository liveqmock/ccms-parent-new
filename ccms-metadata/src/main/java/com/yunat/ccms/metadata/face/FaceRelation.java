package com.yunat.ccms.metadata.face;

/**
 * 前端接口：定义布尔条件关系（AND和OR）
 *
 * @author kevin.jiang 2013-3-18
 */
public class FaceRelation {

	private String key;
	private String name;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
